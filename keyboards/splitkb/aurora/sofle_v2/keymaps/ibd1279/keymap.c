bool encoder_update_user(uint8_t index, bool clockwise) {
    switch (get_highest_layer(layer_state | default_layer_state)) {
        case _ABOVE:
            if (index == 0) {
                // Tab switching
                if (clockwise) {
                    tap_code16(G(S(KC_RBRC)));
                } else {
                    tap_code16(G(S(KC_LBRC)));
                }
            } else {
                // Not yet implemented
            }
            break;
        case _BELOW:
            if (index == 0) {
                if (clockwise) {
                    tap_code(KC_VOLU);
                } else {
                    tap_code(KC_VOLD);
                }
            } else {
                // Media Control
                if (clockwise) {
                    tap_code16(KC_MNXT);
                } else {
                    tap_code16(KC_MPRV);
                }
            }
            break;
        default:
            if (index == 0) {
                if (clockwise) {
                    if (is_page_mode) {
                        tap_code(KC_PAGE_DOWN);
                    } else {
                        tap_code(KC_MS_WH_UP);
                    }
                } else {
                    if (is_page_mode) {
                        tap_code(KC_PAGE_UP);
                    } else {
                        tap_code(KC_MS_WH_DOWN);
                    }
                }
            } else {
                // App switching
                if (!is_alt_tab_active) {
                    is_alt_tab_active = true;
                    register_code(KC_LGUI);
                }
                alt_tab_timer = timer_read();
                if (clockwise) {
                    tap_code16(KC_TAB);
                } else {
                    tap_code16(S(KC_TAB));
                }
            }
            break;
    }
    return false;
}
