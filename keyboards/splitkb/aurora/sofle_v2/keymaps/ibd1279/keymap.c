bool encoder_update_user(uint8_t index, bool clockwise) {
    switch (get_highest_layer(layer_state | default_layer_state)) {
        case _ABOVE:
            if (index == 0) {
                // Tab switching
                if (clockwise) {
                    tap_code16(G(KC_GRV));
                } else {
                    tap_code16(G(KC_TILD));
                }
            }
            break;
        case _BELOW:
            if (index == 0) {
                // Media Control
                if (clockwise) {
                    tap_code16(KC_MNXT);
                } else {
                    tap_code16(KC_MPRV);
                }
            } else {
                // Volume Control
                if (clockwise) {
                    tap_code(KC_VOLU);
                } else {
                    tap_code(KC_VOLD);
                }
            }
            break;
        case _ADJUST:
            if (index == 0) {
                // LED Brightness
                if (clockwise) {
                    rgblight_increase_val();
                } else {
                    rgblight_decrease_val();
                }
            } else {
                // Animation
                if (clockwise) {
                    rgblight_step();
                } else {
                    rgblight_step_reverse();
                }
            }
            break;
        default:
            if (index == 0) {
                // Tab switching
                if (clockwise) {
                    tap_code16(G(S(KC_RBRC)));
                } else {
                    tap_code16(G(S(KC_LBRC)));
                }
            } else {
                // App switching
                if (!is_alt_tab_active) {
                    is_alt_tab_active = true;
                    register_code(KC_LGUI);
                }
                alt_tab_timer = timer_read();
                if (clockwise) {
                    tap_code16(KC_TAB);
                } else {
                    tap_code16(S(KC_TAB));
                }
            }
            break;
    }
    return false;
}
#endif // defined(ENCODER_ENABLE)
